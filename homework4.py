def palindrome(n):
    '''Проверка числа на палиндромность
    '''
    result = n
    # Переменная в которую будем записывать перевернутое число
    rev = 0
    # Создаем цикл в котором переворачиваем число
    while result != 0:
        # Последнею цифру нашего число добавляем в начало переменной
        rev = rev * 10 + result % 10
        result = result // 10
    # Сравниваем данное и перевернутое
    if n == rev:
        return True
    else:
        return False
