#Выведите n-ое число Фибоначчи, используя только временные переменные, циклические операторы и условные операторы. n - вводится.
def fibonacci(n):
    '''Функция для получения н-го числа Фибоначи
    '''
    # Проверяем базовые случаи, когда n равно 1 или 2
    if n == 1:
        return 0
    elif n == 2:
        return 1
    # Инициализируем переменные для хранения двух предыдущих чисел Фибоначчи
    fib1 = 0
    fib2 = 1
    # Инициализируем переменную для хранения текущего числа Фибоначчи
    fib = 0
    # Начинаем цикл с третьего числа Фибоначчи до n-ого
    for i in range(0, n - 2):
        fib = fib1 + fib2
        fib1 = fib2
        fib2 = fib

    return fib
